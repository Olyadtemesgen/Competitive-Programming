class Solution:
    def champagneTower(self, poured: int, query_row: int, query_glass: int) -> float:
        
        # first lets make a dp list of lists that we will store the amount that the glass will hold
        
        dp = [[0 for _ in range(x)] for x in range(1, query_row + 2)]
        
        dp[0][0] = poured
        
        for index in range(query_row):
            
            for before in range(len(dp[index])):
                
                overflowed = ((dp[index][before]) - 1) / 2
                
                if overflowed > 0:
                    dp[index + 1][before] += overflowed
                    dp[index + 1][before + 1] += overflowed
        
        return min(1, dp[query_row][query_glass])
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        